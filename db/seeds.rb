def seed_user(email:, name:, password: "heartwood123")
  User.find_or_create_by!(email_address: email) { |u| u.name = name; u.password = password }
end

def seed_tree(name:, owner:)
  tree = owner.trees.find_by(name: name)
  return tree if tree
  tree = Tree.create!(name: name)
  TreeMembership.create!(user: owner, tree: tree, role: "owner")
  tree
end

def seed_member(tree:, user:, role: "editor")
  TreeMembership.find_or_create_by!(tree: tree, user: user) { |m| m.role = role }
end

def pers(tree, given_names:, surname:, sex:, nickname: nil, prefix: nil, suffix: nil)
  tree.people.find_or_create_by!(given_names: given_names, surname: surname) do |p|
    p.sex = sex; p.nickname = nickname; p.name_prefix = prefix; p.name_suffix = suffix
  end
end

def evt(subject, kind:, date: nil, value: nil)
  subject.events.find_or_create_by!(kind: kind, date_raw: date) { |e| e.value = value }
end

def fam(tree, partners: [], children: [])
  partner_ids = partners.map(&:id).sort
  found = tree.families.select { |f| f.partners.pluck(:id).sort == partner_ids }.first
  return found if found
  f = tree.families.create!
  f.partners << partners
  f.children  << children
  f
end

def src(tree, title:, citation_text: nil, url: nil)
  tree.sources.find_or_create_by!(title: title) { |s| s.citation_text = citation_text; s.url = url }
end

def cite(source, citable)
  Citation.find_or_create_by!(source: source, citable: citable)
end

def bio(person, html)
  return if person.biography.present?
  person.update!(biography: html)
end

SEED_IMAGES = Rails.root.join("db/seed_images")

def photo(person, filename)
  return if person.avatar.attached?
  path = SEED_IMAGES.join(filename)
  return puts "    фото отсутствует: #{filename}" unless path.exist?
  ext  = File.extname(filename).downcase
  mime = { ".png" => "image/png", ".jpg" => "image/jpeg", ".jpeg" => "image/jpeg" }.fetch(ext, "image/jpeg")
  person.avatar.attach(io: path.open, filename: filename, content_type: mime)
  puts "    фото → #{person.display_name}"
rescue => e
  puts "    фото пропущено (#{person.display_name}): #{e.message.truncate(70)}"
end

